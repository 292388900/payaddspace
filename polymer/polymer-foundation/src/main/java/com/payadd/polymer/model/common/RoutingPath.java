package com.payadd.polymer.model.common;

public class RoutingPath {
	private String protocolCode;
	private String productCode;
	private String channelCode;
	private String agencyCode;
	private String channelInterfaceCode;
	public String getProtocolCode() {
		return protocolCode;
	}
	public void setProtocolCode(String protocolCode) {
		this.protocolCode = protocolCode;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getChannelCode() {
		return channelCode;
	}
	public void setChannelCode(String channelCode) {
		this.channelCode = channelCode;
	}
	public String getAgencyCode() {
		return agencyCode;
	}
	public void setAgencyCode(String agencyCode) {
		this.agencyCode = agencyCode;
	}
	public String getChannelInterfaceCode() {
		return channelInterfaceCode;
	}
	public void setChannelInterfaceCode(String channelInterfaceCode) {
		this.channelInterfaceCode = channelInterfaceCode;
	}
	
	
}
